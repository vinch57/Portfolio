    <h2>Les conférences</h2>
    <hr class="divider">
    <p>
        J’ai pu assister à une conférence sur l’internet des objets mettant en avant des retours d’expériences et présidée par Laurent Marichou, 
        directeur Open Innovation chez Orange, Nicolas Lesbonnec, 
        développeur à Sigfox et Loic Texier, fondateur et directeur général à Celio.
    </p>
    <img class="img-responsive" src="img/confinterobjet.jpg"/><em>Conférence sur l'internet des objets</em>
    <br/>
    <p>Cette conférence avait pour but de présenter la place qu’occupent aujourd’hui les objets connectés, ce qui rassemble l’ensemble de la domotique en partie et de débattre sur le sujet.</p>
    <br/>
    <br/>
    <p>
        Enfin j’ai assisté à un Workshop présidé par Nicolas Lesbonnec visant à présenter un système de transmission de messages par l’intermédiaire d’émetteurs,
        pour envoyer des signaux de température par exemple.
    </p>
    <p>
        La portée des données est très bonne, en revanche cela impacte fort sur la consommation énergétique, 
        en effet plus la portée est bonne, plus cela consomme d’énergie, 
        ces capteurs fonctionnent avec des composants standards du marché (puces, carte Raspberry PI ou carte Arduino) 
        qui sont des composants suffisamment puissants pour faire fonctionner le système.
    </p>
    <p>
        La réception des messages est instantanée et utilise la méthode du push c’est-à-dire que les données sont directement implantées dans le serveur.
    </p>
    <p>Les capteurs qui étaient présentés étaient en outre un capteur de température et un bouton poussoir 
        permettant d’émettre un signal à un moment donné.
    </p>
