    <p>J’ai aussi noté la présence de games pads évolués permettant de jouer à des jeux de combat en partie.</p>
    <p>
        Ce qui m’a le plus marqué et intéressé est le stand de la société My Music Teacher, 
        qui présentait leur logiciel d’apprentissage de la guitare, 
        il suffit de brancher sa guitare électrique sur son pc par l’intermédiaire d’une carte son et le logiciel passait la partition/tablature sur l’écran du pc,
        le but est de jouer en même temps que la partition défile (on peut naturellement modifier le tempo), 
        le logiciel calcule en temps réel ce que joue le musicien et lui donne un résultat sur sa progression (les notes qui tombent juste ou non), 
        j’ai pu échanger avec le créateur du logiciel sur le fonctionnement du produit.
    </p>
