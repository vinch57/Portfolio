    <h2>Dans le salon</h2>
    <hr class="divider">
    <p>
        Cette visite s’est déroulée au Parc des Expositions Metz Métropole, j’ai pu y découvrir de nouvelles avancées technologiques pour les jeux vidéo, par exemple un jeu de voitures piloté par un vrai volant et pédales provoquant des sensations proches de la réalité,
        de plus les lunettes de réalité virtuelle embellissaient davantage l’expérience utilisateur, une autre démonstration de ces lunettes était présente avec la simulation d’être dans Star Wars avec la mise à disposition bien sûr, d'un sabre laser !
