    <p>
        L'atout majeur de Lima, c'est qu'il permet de créer son propre cloud privé, mais aussi la centralisation puisque vous pouvez très bien synchroniser 2 Limas ensemble... L'un chez vous et l'autre chez vos parents ou au bureau. Ainsi vos données sont toujours en sécurité, les données stockées sur l'un sont automatiquement dupliquées sur l'autre.
        Et comme de toute façon, elles sont quand même mises en cache sur votre ordinateur local, en cas de crash, vous pouvez quand même les restaurer sur un nouveau disque dur.
        Les différents appareils communiquent avec le Lima grâce à une technologie de type Peer-to-Peer (P2P) sécurisée via un chiffrement AES 256 bits.
        Disponible en différents coloris, ce petit boitier est affiché au prix de 99€.
