        Lancé via l'entreprise Kickstarter il y a un petit moment, le projet Lima est une bonne alternative aux NAS et aux solutions de cloud déjà présentes sur le marché de l'informatique.
        Ce petit boitier transforme n'importe quel disque dur USB en disque réseau accessible depuis n'importe où avec une facilité d'utilisation accrue (ce produit est en partie plug-and-play) ce qui en fait un compromis très attractif entre le NAS,
        plus compliqué à utiliser, et les services Cloud (Dropbox, Google Drive, OneDrive, etc) qui sont simples d’emploi, mais qui peuvent poser des problèmes de confidentialité et de vie privée (les données ne sont pas stockées chez l’utilisateur).
