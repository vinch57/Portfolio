        mais dispose d’un lecteur multimédia complet pour les photos (avec vignettes), l’audio (avec des classements par artiste) 
        et les vidéos (avec des vignettes d’affiche et même des fiches d’information). Un mode « hors connexion » est prévu pour rapatrier des fichiers en tout genre (vidéos, musiques, etc.) sur l’appareil mobile afin d’en profiter sans être connecté. Autre avantage, 
        les photos prises avec votre mobile sont automatiquement stockées sur le Lima si une connexion Internet est présente. 
        L’application lit également les documents bureautiques (Word, Excel, Powerpoint, PDF). De plus, elle est compatible avec la technologie Chromecast de Google, 
        ce qui permet d’envoyer l’audio et la vidéo sur un téléviseur ou un boîtier multimédia compatible.
