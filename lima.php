        Un système de fichier spécifique est alors mis en place sur le(s) disque(s) et vous n'avez plus qu'à installer le driver qui va bien sous OSX, 
        Windows, Ubuntu ou installer l'application mobile Lima pour accéder à cet espace de stockage.
